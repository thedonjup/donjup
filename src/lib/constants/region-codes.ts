/** 전국 모든 시군구 코드를 [code, sigunguName, sidoShortName] 튜플 배열로 반환 */
export function getAllSigunguCodes(): [string, string, string][] {
  const result: [string, string, string][] = [];
  for (const sido of Object.values(REGION_HIERARCHY)) {
    for (const [code, name] of Object.entries(sido.sigungu)) {
      result.push([code, name, sido.shortName]);
    }
  }
  return result;
}

/** slug로 시/도 조회 (e.g. "seoul" -> 서울 Sido) */
export function getSidoBySlug(slug: string): Sido | undefined {
  return Object.values(REGION_HIERARCHY).find((s) => s.slug === slug);
}

/** 5자리 코드의 앞 2자리로 시/도 조회 */
export function getSidoForCode(code: string): Sido | undefined {
  const prefix = code.slice(0, 2);
  return REGION_HIERARCHY[prefix];
}

/** 5자리 시군구 코드로 시군구명 조회 */
export function getSigunguName(code: string): string | undefined {
  const sido = getSidoForCode(code);
  return sido?.sigungu[code];
}

